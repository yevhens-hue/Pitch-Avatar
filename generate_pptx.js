// Master slide design similar to the dark theme they had
pres.defineSlideMaster({
  title: 'MASTER_SLIDE',
  background: { color: '0F172A' },
  objects: [
    { rect: { x: 0, y: 0, w: '100%', h: 0.1, fill: { color: '0076FF' } } } // top border
  ]
});

slidesData.forEach((data, index) => {
  let slide = pres.addSlide({ masterName: 'MASTER_SLIDE' });

  // Title
  slide.addText(data.title, {
    x: 0.5, y: 0.5, w: '90%', h: 0.8, 
    fontSize: 32, bold: true, color: 'F1F5F9', fontFace: 'Arial'
  });

  // Subtitle
  slide.addText(data.subtitle, {
    x: 0.5, y: 1.3, w: '90%', h: 0.5, 
    fontSize: 16, bold: true, color: '0076FF', fontFace: 'Arial'
  });

  // Script (Body Text)
  slide.addText(data.script, {
    x: 0.5, y: 2.0, w: '90%', h: 1.5, 
    fontSize: 18, color: '94A3B8', fontFace: 'Arial', italic: true, valign: 'top'
  });

  // Bullets
  let bulletText = data.bullets.map(b => ({ text: b, options: { bullet: true, color: '60A5FA', fontSize: 18 } }));
  slide.addText(bulletText, {
    x: 0.5, y: 3.5, w: '90%', h: 1.5, 
    fontFace: 'Arial', fill: { color: '1E293B' }, color: 'F1F5F9',
    margin: 10, valign: 'middle'
  });

  // Slide Number
  slide.addText(`${index + 1} / ${slidesData.length}`, {
    x: '85%', y: '85%', w: '10%', h: 0.5, 
    fontSize: 12, color: '475569', align: 'right'
  });
});

